def get_first_item_from(list, first_index, has_first = True):
    find_index = -1
    slice_list = list[first_index:] if has_first == True else list[first_index+1:]


    for list_index,list_item in enumerate(slice_list):
        if list_index == 0:
            continue
        if list_item.find('@') != -1:
            find_index = list_index
            break


    return slice_list[:find_index] if find_index != -1 else slice_list

    
def get_insert_sql(table_name, objs):
    key_list = objs.keys()
    value_list = objs.values()

    value_arr = []

    for item in value_list:
        if type(item) == int:
            value_arr.append('%d' %item) 
        elif type(item) == str:
            value_arr.append('''
                        '%s'
                        ''' % item)
        elif item == None:
            value_arr.append('NULL')

    sqls = 'insert into %s (%s) values (%s)' % (table_name, ','.join(key_list), ','.join(value_arr))
    return sqls

def get_update_sql(table_name, objs, where_field = ''):
    value_arr = []
    where_sql = ''

    if where_field != '':
        field_val = objs[where_field]
        del objs[where_field]
        if type(field_val) == int:
            where_sql = 'where %s = %d' % (where_field, field_val)
        elif type(field_val) == str:
            where_sql = 'where %s = "%s"' % (where_field, field_val)

    for key,val in objs.items():
        if type(val) == int:
            value_arr.append('%s=%d' % (key, val))
        elif type(val) == str:
            value_arr.append('%s="%s"' % (key, val))
        elif val == None:
            value_arr.append('%s=NULL' % (key))

    if where_sql != '':
        sqls = 'update %s set %s %s' % (table_name, ','.join(value_arr), where_sql)
    else:
        sqls = 'update %s set %s' % (table_name, ','.join(value_arr))
    return sqls

